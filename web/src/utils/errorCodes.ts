/**
 * 根据错误码获取用户友好的错误消息
 * @param code 后端返回的错误码
 * @param defaultMsg 默认消息（未匹配到映射时使用）
 */
export function getErrorMessage(code: number | undefined, defaultMsg = '操作失败，请稍后重试'): string {
  if (code === undefined || code === null) {
    return defaultMsg;
  }
  return errorCodeMap[code] ?? `错误码 ${code}`;
}
